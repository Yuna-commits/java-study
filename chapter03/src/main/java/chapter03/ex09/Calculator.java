package chapter03.ex09;

public class Calculator {
	public static int add(int n1, int n2) {
		return n1 + n2;
	}

	public static int subtract(int n1, int n2) {
		return n1 - n2;
	}

	public static int multiply(int n1, int n2) {
		return n1 * n2;
	}

	public static int divide(int n1, int n2) {
		return n1 / n2;
	}
}
